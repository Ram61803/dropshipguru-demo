import { readdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const productsDir = path.join(root, "public", "products");
const outFile = path.join(root, "lib", "demo", "products", "image-manifest.ts");

const IMAGE_EXT = new Set([".jpg", ".jpeg", ".png", ".webp", ".jfif", ".gif"]);

function scanFolder(dir) {
  const folders = {};

  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;

    const folderName = entry.name;
    const folderPath = path.join(dir, folderName);
    const files = readdirSync(folderPath, { withFileTypes: true })
      .filter((f) => f.isFile() && IMAGE_EXT.has(path.extname(f.name).toLowerCase()))
      .map((f) => f.name)
      .sort((a, b) => a.localeCompare(b));

    if (files.length > 0) {
      folders[folderName] = files;
    }
  }

  return folders;
}

const folders = scanFolder(productsDir);

const fileBody = `/** Auto-generated by scripts/sync-product-images.mjs — do not edit manually */
export const PRODUCT_IMAGE_FOLDERS = ${JSON.stringify(folders, null, 2)} as const;

export type ProductImageFolder = keyof typeof PRODUCT_IMAGE_FOLDERS;
`;

writeFileSync(outFile, fileBody, "utf8");
console.log(
  `Synced ${Object.values(folders).flat().length} product images across ${Object.keys(folders).length} folders.`,
);
